#define GAME2_FRAME_TIME_MS 30
#define GAME2_GRAVITY 1
#define GAME2_JUMP_FORCE -12
#define GAME2_GROUND_Y 180

MenuState Game2_Run(void)
{
    int game2_state = 0;

    int game2_dino_y = GAME2_GROUND_Y;
    int game2_dino_vy = 0;
    int game2_on_ground = 1;
    int game2_ducking = 0;

    int game2_score = 0;
    int game2_high_score = 0;
    int game2_record_beaten = 0;

    int game2_lives = 1;

    int game2_speed = 8;

    int game2_cactus_x = 240;
    int game2_cactus_type = 1;

    int game2_anim_frame = 0;
    int game2_anim_counter = 0;

    srand(HAL_GetTick());

    while (1)
    {
        uint32_t frame_start = HAL_GetTick();

        Input_Read();
        Joystick_Read(&joystick_cfg, &joystick_data);

         UserInput joy = Joystick_GetInput(&joystick_data);

        // back to Menu
        if (current_input.btn3_pressed)
        {
            return MENU_STATE_HOME;
        }

        // Sttart screen
        if (game2_state == 0)
        {
            LCD_Fill_Buffer(0);

            LCD_printString("LONELY REX", 40, 80, 1, 3);
            LCD_printString("UP TO START", 50, 140, 1, 2);

            LCD_Refresh(&cfg0);

            if (joy.direction == N)
            {
                game2_state = 1;

                game2_dino_y = GAME2_GROUND_Y;
                game2_dino_vy = 0;
                game2_on_ground = 1;
                game2_ducking = 0;

                game2_score = 0;
                game2_lives = 1;
                game2_speed = 8;
                game2_record_beaten = 0;

                game2_cactus_x = 240;
            }

            HAL_Delay(50);
            continue;
        }

        // game over screen
        if (game2_state == 2)
        {
            LCD_Draw_Rect(30, 70, 180, 100, 0, 1);

            LCD_printString("GAME OVER", 50, 90, 1, 3);
            LCD_printString("UP TO RESTART", 25, 140, 1, 2);

            LCD_Refresh(&cfg0);

            if (joy.direction == N)
             {
                game2_state = 1;

                game2_dino_y = GAME2_GROUND_Y;
                game2_dino_vy = 0;
                game2_on_ground = 1;
                game2_ducking = 0;

                game2_score = 0;
                game2_lives = 1;
                game2_speed = 8;
                game2_record_beaten = 0;

                game2_cactus_x = 240;
            }

            HAL_Delay(100);
            continue;
        }

        // jump
        if (joy.direction == N && game2_on_ground)
        {
            game2_dino_vy = GAME2_JUMP_FORCE;
            game2_on_ground = 0;

            buzzer_tone(&buzzer_cfg, 1200, 60);
            HAL_Delay(40);
            buzzer_off(&buzzer_cfg);
        }

        // duck
        if (joystick_data.y_processed > 1500 && game2_on_ground)
        {
            game2_ducking = 1;
        }
        else
        {
            game2_ducking = 0;
        }

        // gravity
        int falling_gravity =
            GAME2_GRAVITY + 4 + (game2_speed - 8);

        if (game2_dino_vy < 0)
        {
            game2_dino_vy += GAME2_GRAVITY;
        }
         else
        {
            game2_dino_vy += falling_gravity;
        }

        game2_dino_y += game2_dino_vy;

        // Ground collision
        if (game2_dino_y >= GAME2_GROUND_Y)
        {
            game2_dino_y = GAME2_GROUND_Y;
            game2_dino_vy = 0;
            game2_on_ground = 1;
        }

        // move cactus
        game2_cactus_x -= game2_speed;

        // respawn cactus
        if (game2_cactus_x +
            ((game2_cactus_type - 1) * 18) < -24)
        {
            game2_cactus_type = 1 + (rand() % 3);

            game2_cactus_x = 240;
            game2_score++;

        // high score sound
            if (game2_score > game2_high_score)
            {
                if (!game2_record_beaten)
                {
                    buzzer_tone(&buzzer_cfg, 2000, 60);
                    HAL_Delay(60);

                    buzzer_tone(&buzzer_cfg, 2600, 80);
                    HAL_Delay(80);

                    buzzer_off(&buzzer_cfg);

                    game2_record_beaten = 1;
                    }

                game2_high_score = game2_score;
            }

            // milestone sound
            if (game2_score % 10 == 0)
            {
                buzzer_tone(&buzzer_cfg, 1500, 40);
                HAL_Delay(40);

                buzzer_off(&buzzer_cfg);
            }

            // increase difficulty
            if (game2_score % 5 == 0)
            {
                game2_speed++;
            }
        }

        // dino hitbox
        int dino_x = 35;
        int dino_w = 24;

        int dino_h;

        if (game2_ducking)
        {
            dino_h = 14;
        }
        else
        {
            dino_h = 24;
        }

        int dino_top = game2_dino_y - dino_h;

        // cactus hitbox
        int cactus_y = GAME2_GROUND_Y - 18;
        int cactus_w = 18;

        // collision
        for (int i = 0; i < game2_cactus_type; i++)
        {
            int cx = game2_cactus_x + (i * 18);

            if (dino_x < cx + cactus_w &&
                dino_x + dino_w > cx &&
                dino_top < cactus_y + 10 &&
                dino_top + dino_h > cactus_y)
            {
                game2_lives--;

                buzzer_tone(&buzzer_cfg, 400, 80);
                HAL_Delay(240);

                buzzer_off(&buzzer_cfg);

                if (game2_lives <= 0)
                {
                    buzzer_tone(&buzzer_cfg, 700, 80);
                    HAL_Delay(120);

                    buzzer_tone(&buzzer_cfg, 250, 80);
                    HAL_Delay(460);

                    buzzer_off(&buzzer_cfg);

                    game2_state = 2;
                }

                game2_cactus_x = 240;
                break;
            }
        }

        // Runnning animation
        if (game2_on_ground && !game2_ducking)
        {
            game2_anim_counter++;

            if (game2_anim_counter > 2)
            {
                game2_anim_counter = 0;
                game2_anim_frame = !game2_anim_frame;
            }
        }
        else if (!game2_on_ground)
        {
            game2_anim_frame = 0;
        }

         LCD_Fill_Buffer(0);

        // ground
        LCD_Draw_Rect(0, GAME2_GROUND_Y, 240, 3, 1, 1);

         // choose sprite
        const uint8_t* current_dino;

        if (game2_ducking)
        {
            current_dino = dino_duck_sprite;
        }
         else if (game2_anim_frame == 0)
        {
            current_dino = dino_sprite_1;
        }
        else
        {
            current_dino = dino_sprite_2;
        }

        int dino_draw_y;

        if (game2_ducking)
        {
            dino_draw_y = game2_dino_y - 38;
        }
        else
        {
            dino_draw_y = game2_dino_y - 54;
        }

        // draw dino
        LCD_Draw_Sprite_Scaled(20,dino_draw_y,32,32,current_dino,2);

        // draw cactus group
        for (int i = 0; i < game2_cactus_type; i++)
        {
            int cx = game2_cactus_x + (i * 18);
            int cy = GAME2_GROUND_Y - 24;

            LCD_Draw_Sprite_Scaled(cx,cy,8,6,cactus_sprite,3  );
        }

        // UI
        char text[20];

        sprintf(text, "High: %d", game2_high_score);
        LCD_printString(text, 10, 10, 1, 2);

        sprintf(text, "Score: %d", game2_score);
        LCD_printString(text, 120, 10, 1, 2);

        LCD_Refresh(&cfg0);

        // Stable frame timing
        uint32_t frame_time = HAL_GetTick() - frame_start;

        if (frame_time < GAME2_FRAME_TIME_MS)
        {
            HAL_Delay(GAME2_FRAME_TIME_MS - frame_time);
        }
    }
}

  
     
